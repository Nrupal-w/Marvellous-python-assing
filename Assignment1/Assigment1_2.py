def ChkNum(n):
    if n%2==0:
        print("Even Number")
    else:
        print("Odd Number")
    
a=int(input("Enter a number"))
ChkNum(a)

# This program takes an integer input from the user and checks whether it is even or odd using the ChkNum function.
# It prints "Even Number" if divisible by 2, otherwise "Odd Number".
